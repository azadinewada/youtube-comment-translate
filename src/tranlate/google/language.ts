export const getGoogleLanguageSupport = (lang: string): string => {
  if (isValidKey(lang, whiteMapping)) {
    return whiteMapping[lang]
  }
  // 只对照第一位
  const l = lang.split('-')[0]
  if (googleSupport.indexOf(l) !== -1){
    return l
  }
  console.error('不支持当前语言：'+ lang +'，使用默认语言：zh-CN')
  return 'zh-Hans-CN'
}